#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*next;

	if (lst != NULL)
	{
		next = lst;
		while (next)
		{
			(*f)(next->content);
			next = next->next;
		}
	}
}
