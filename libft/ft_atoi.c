#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	operators;
	int	nb;

	i = 0;
	nb = 0;
	operators = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
		operators++;
	i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10;
		nb = nb + (str[i] - 48);
		i++;
	}
	if (operators % 2 == 1)
		nb = -nb;
	return (nb);
}